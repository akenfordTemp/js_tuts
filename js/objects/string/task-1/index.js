/*
. Дана строка 'aaa bbb ccc'. Вырежите из нее слово 'bbb' тремя разными
способами (через substr, substring, slice).
 */
let strSubstr = 'aaa bbb ccc';
alert(strSubstr.substr(4, 3));

let strSubstring = 'aaa bbb ccc';
alert(strSubstring.substring(4, 7));

let strSlice = 'aaa bbb ccc';
alert(strSlice.slice(4, 7));






